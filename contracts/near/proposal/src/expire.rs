use crate::*; // loading all defined crates, structs and functions from the root crate which is lib.rs in our case






#[near_bindgen]
impl ProposalContract{

    #[payable]
    pub fn expire_proposal(&mut self, proposal_id: ProposalId) -> Option<Proposal>{
      
        assert_one_yocto();
        let caller_id = env::predecessor_account_id();
        if let Some(mut proposal) = self.proposals_by_id.get(&proposal_id){
          
          if caller_id != proposal.collection_creator_id{
            env::log_str("SMARTIES : Caller Is Not The Collection Creator");
            None
          } else{
            proposal.expire_at = env::block_timestamp();
            proposal.is_expired = true;
            self.proposals_by_id.insert(&proposal_id, &proposal);
            Some(proposal)
          }

      } else{
          env::log_str("SMARTIES : Proposal With This Id Is Not Exist");
          None
      }

    }
}
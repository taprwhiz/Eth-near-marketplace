#[near_bindgen]
impl ProposalContract{

    //// return all proposals for a specific collection
    pub fn get_all_proposals_by_collection(&self, collection_name: String) -> Vec<Option<GetProposal>>{

        let mut proposals = vec![];
        for (_, p) in self.proposals_by_id.iter().enumerate(){
            if p.1.collection_name == collection_name{ 
                proposals.push(
                    Some(GetProposal{
                        proposal_id: p.0,
                        proposal: p.1
                    })
                )
            } else{
                continue;
            }
        }

        proposals

    }


}
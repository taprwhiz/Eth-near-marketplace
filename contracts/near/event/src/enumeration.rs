#[near_bindgen]
impl EventContract{

    //// return all events for a specific collection
    pub fn get_all_events_by_collection(&self, collection_name: String) -> Vec<Option<GetEvent>>{

        let mut events = vec![];
        for (_, e) in self.events_by_id.iter().enumerate(){
            if e.1.collection_name == collection_name{ 
                events.push(
                    Some(GetEvent{
                        event_id: e.0,
                        event: e.1
                    })
                )
            } else{
                continue;
            }
        }

        events

    }


}
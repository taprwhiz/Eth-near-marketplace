use crate::*; // loading all defined crates, structs and functions from the root crate which is lib.rs in our case






#[near_bindgen]
impl EventContract{

    #[payable]
    pub fn expire_event(&mut self, event_id: EventId) -> Option<Event>{
      
        assert_one_yocto();
        let caller_id = env::predecessor_account_id();
        if let Some(mut event) = self.events_by_id.get(&event_id){
          
          if caller_id != event.collection_creator_id{
            env::log_str("SMARTIES : Caller Is Not The Collection Creator");
            None
          } else{
            event.expire_at = env::block_timestamp();
            event.is_expired = true;
            self.events_by_id.insert(&event_id, &event);
            Some(event)
          }

      } else{
          env::log_str("SMARTIES : Event With This Id Is Not Exist");
          None
      }

    }
}
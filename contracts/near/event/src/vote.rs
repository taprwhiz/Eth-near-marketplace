use crate::*; // loading all defined crates, structs and functions from the root crate which is lib.rs in our case






#[near_bindgen]
impl EventContract{

    //// vote on a specific event
    #[payable]
    pub fn vote(&mut self, event_id: EventId, participant: Participant) -> Option<Event>{

        assert_one_yocto(); //// voter must deposit 1 yocto near
        if let Some(mut event) = self.events_by_id.get(&event_id){

            let mut participants = event.participants;
            let index = participants.iter().position(|p| p.nft_owner_id == participant.nft_owner_id); //-- this owner has alreay voted to this event
            if index == None{
              participants.push(participant);
            }
            event.participants = participants;
            self.events_by_id.insert(&event_id, &event);
            Some(event)


        } else{
          env::log_str("SMARTIES : Event With This Id Is Not Exist");
          None
        }



    }




}